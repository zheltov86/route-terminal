def инн():
    return ''.join([str(random.randint(0, 9)) for _ in range(10)])

def кпп(инн_значение):
    return инн_значение[:4] + str(random.randint(10, 99)) + '01'

def телефон():
    return f"+7 ({random.randint(300,999)}) {random.randint(100,999)}-{random.randint(10,99)}-{random.randint(10,99)}"

def email(наименование):
    slug = наименование.lower().replace(' ', '').replace('«', '').replace('»', '').replace('"', '')[:12]
    return f"info@{slug}.{random.choice(['mail.ru', 'yandex.ru', 'company.ru'])}"

def адрес():
    return f"г. Москва, ул. {random.choice(УЛИЦЫ)}, д. {random.randint(1,100)}"

def контрагенты(count=5):
    """Список контрагентов"""
    результат = []
    used = set()
    for _ in range(count):
        name = random.choice(КОНТРАГЕНТЫ_НАЗВАНИЯ)
        while name in used:
            name = random.choice(КОНТРАГЕНТЫ_НАЗВАНИЯ)
        used.add(name)
        инн_значение = инн()
        результат.append({
            "Идентификатор": str(uuid.uuid4()),
            "Наименование": name,
            "ИНН": инн_значение,
            "КПП": кпп(инн_значение),
            "ЮридическийАдрес": адрес(),
            "ФактическийАдрес": адрес(),
            "Телефон": телефон(),
            "Email": email(name),
            "КонтактноеЛицо": random.choice(["Иванов И.И.", "Петров П.П.", "Сидоров С.С.", "Козлов К.К.", "Морозов А.В."]),
            "ДатаРегистрации": (datetime.now() - timedelta(days=random.randint(100, 3000))).strftime("%Y-%m-%dT%H:%M:%S"),
            "ОКВЭД": random.choice(["46.46", "46.66", "46.51", "43.99", "62.01", "62.02"]),
            "Статус": random.choice(СТАТУСЫ_К),
        })
    return результат

def заказ(контрагент):
    """Один заказ для контрагента"""
    кол = random.randint(2, 8)
    позиции = []
    общая = 0
    общ_ндс = 0
    for i in range(1, кол + 1):
        кол_во = round(random.uniform(1, 50), 0)
        цена = round(random.uniform(100, 50000), 2)
        сумма = round(кол_во * цена, 2)
        ставка = random.choice(["20%", "10%", "0%"])
        ндс = round(сумма * 0.2 if ставка == "20%" else (сумма * 0.1 if ставка == "10%" else 0), 2)
        общая += сумма
        общ_ндс += ндс
        позиции.append({
            "НомерСтроки": i,
            "Номенклатура": random.choice(ТОВАРЫ),
            "Количество": кол_во,
            "ЕдиницаИзмерения": random.choice(ЕДИНИЦЫ),
            "Цена": цена,
            "Сумма": сумма,
            "СтавкаНДС": ставка,
            "СуммаНДС": ндс,
        })

    return {
        "Номер": f"ЗК-{random.randint(10000, 99999)}-{datetime.now().strftime('%y')}",
        "Дата": (datetime.now() - timedelta(days=random.randint(0, 30))).strftime("%Y-%m-%dT%H:%M:%S"),
        "КонтрагентИд": контрагент["Идентификатор"],
        "КонтрагентНаименование": контрагент["Наименование"],
        "Организация": random.choice(["ООО «НашаКомпания»", "АО «ГлавныйОфис»", "ООО «Центр»"]),
        "ХозяйственнаяОперация": random.choice(ОПЕРАЦИИ),
        "Валюта": "RUB",
        "СуммаДокумента": round(общая, 2),
        "СуммаНДС": round(общ_ндс, 2),
        "Статус": random.choice(СТАТУСЫ_З),
        "Позиции": позиции,
        "Комментарий": f"Заказ от {datetime.now().strftime('%d.%m.%Y')} — {контрагент['Наименование']}",
    }

def заказы(count=10, контрагент_ид=None):
    """Список заказов"""
    ктс = контрагенты(max(count, 5))
    результат = []
    for _ in range(count):
        кт = random.choice(ктс)
        if контрагент_ид:
            кт = next((к for к in ктс if к["Идентификатор"] == контрагент_ид), кт)
        результат.append(заказ(кт))
    return результат
